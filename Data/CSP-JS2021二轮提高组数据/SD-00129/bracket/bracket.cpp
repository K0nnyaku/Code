const int mod=1e9+7;
const int N=500+10;
int n,k,ans=0;
string s;
int L[N],R[N],lk[N],rk[N];
stack<int> q;
inline bool check()
{
	while (!q.empty()) q.pop();
	int kp=0;
	bool xx=0;
	for (int i=0;i<n;i++)
	{
		kp=(s[i]=='*')?kp+1:0;
		if (kp>k) return 0;
		if (s[i]=='(') q.push(1);
		else if (s[i]==')')
		{
			if (q.empty()) return 0;
			q.pop();
		}
		if (q.empty()&&kp) 
		{
			if (xx) return 0;
			xx=1;
		}
		if (q.empty()&&!kp) xx=0;
	}
	return q.empty();
}
